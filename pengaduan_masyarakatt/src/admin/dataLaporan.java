    public void showLaporann(){
        DefaultTableModel tabelM = (DefaultTableModel) tLaporan.getModel();
        for(int i=0; i < this.dataLap.length; i++) {
            TampilModel tampilLaporan = this.dataLap[i];
            tabelM.addRow(new String[] {
                tampilLaporan.ColumnD("id_pengaduan"),
                tampilLaporan.ColumnD("nama_masyarakat"),
                tampilLaporan.ColumnD("tgl_pengaduan"),
                tampilLaporan.ColumnD("status"),
            });
        }
    }
    

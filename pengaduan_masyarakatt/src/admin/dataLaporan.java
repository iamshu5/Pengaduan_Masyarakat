    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int pilihRow = tLaporan.getSelectedRow();
        if(pilihRow >= 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "WARNING! Hapus data ini?");
            if(confirm == 0) {
                String id_p = this.dataLap[pilihRow].ColumnD("id_pengaduan");
                boolean hapus = this.LaporanM.delete(id_p);
                
                if(hapus) {
                    stackFunction.tampilAlert(this, 1, "SUCCESS! Data Telah dihapus");
                    stackFunction.hapusTabelRow(tLaporan, pilihRow);
                    this.dataLap = this.LaporanM.laporanPetugas();
                } else {
                    stackFunction.tampilAlert(this, 0, "UPS ERROR! Tidak dapat menghapus data");
                }
            }
            
        } else {
            stackFunction.tampilAlert(this, 0, "Pilih salah satu data jika ingin menghapus");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    

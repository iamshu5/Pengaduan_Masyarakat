// Button Verifikasi
    private void btn_verifikasiLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verifikasiLaporanActionPerformed
        int pilihRow = tLaporan.getSelectedRow();
        if(pilihRow>=0) {
            TampilModel pilihLaporan = this.dataLap[pilihRow];
                if(pilihLaporan.ColumnD("status").equals("0")) {
                    String id_pengaduan = this.dataLap[pilihRow].ColumnD("id_pengaduan");
                    boolean verifikasi = this.LaporanM.verification(id_pengaduan);
                        
                    if(verifikasi) {
                        stackFunction.tampilAlert(this, 1, "VERIFIED! Data Laporan terverifikasi");
                        setVisible(false);
                        new dataLaporan(this.user).setVisible(true);
                    }else{
                        stackFunction.tampilAlert(this, 0, "ERROR! Tidak Terverifkasi");
                    }
                }else{
                    stackFunction.tampilAlert(this, 0, "Already verified!");
                }
        }else{
            stackFunction.tampilAlert(this, 0, "Pilih salah satu data Laporan");
        }
    }//GEN-LAST:event_btn_verifikasiLaporanActionPerformed


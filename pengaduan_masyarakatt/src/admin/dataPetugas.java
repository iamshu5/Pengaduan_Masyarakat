    public void tampilDataP() {
        DefaultTableModel tModel = (DefaultTableModel) tPetugas.getModel();
        for(int i=0; i < this.dataP.length; i++) {
            TampilModel cDPetugas = this.dataP[i];
            tModel.addRow(new String[] {
                cDPetugas.ColumnD("id_petugas"),
                cDPetugas.ColumnD("nama_petugas"),
                cDPetugas.ColumnD("username"),
                cDPetugas.ColumnD("level"),
            });
        }
    }
    

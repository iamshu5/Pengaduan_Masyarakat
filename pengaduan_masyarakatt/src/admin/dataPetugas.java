    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int pilihRow = tPetugas.getSelectedRow();
        if(pilihRow >= 0) {
            int konfirmasi = JOptionPane.showConfirmDialog(this, "WARNING! Hapus data?");
            if(konfirmasi == 0) {
                String id_petugas = this.dataP[pilihRow].ColumnD("id_petugas");
                boolean hapus = this.PetugasM.delete(id_petugas);
                
                if(hapus) {
                    stackFunction.tampilAlert(this, 1, "SUCCESS! Data telah dihapus");
                    stackFunction.hapusTabelRow(tPetugas, pilihRow);
                    this.dataP = this.PetugasM.Petugas();
                } else {
                    stackFunction.tampilAlert(this, 0, "ERROR! Data tidak dapat dihapus");
                }
            }
            
        } else {
            stackFunction.tampilAlert(this, 0, "Harap pilih data petugas terlebih dahulu!");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        int pilihRow = tPetugas.getSelectedRow();
        if(pilihRow>=0) {
            TampilModel pilihPetugas = this.dataP[pilihRow];
            setVisible(false);
            new editPetugas(this.user, pilihPetugas).setVisible(true);
            
        } else {
            stackFunction.tampilAlert(this, 0, "Pilih salah satu data yang ingin diedit");
        }
        
    }//GEN-LAST:event_btn_editActionPerformed


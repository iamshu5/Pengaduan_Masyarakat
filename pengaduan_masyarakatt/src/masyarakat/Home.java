    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        String sub = kSubjek.getText();
        String isiLap = kisiLap.getText();
        
        boolean kirim = this.LaporanM.tambahLap(new String[]{
            this.user.ColumnD("nik"),
            stackFunction.tanggal(),
            sub,
            isiLap,
            "0",    
        });
        if(kirim) {
            stackFunction.tampilAlert(this, 1, "Laporan Success Terkirim!");
            this.btnResetActionPerformed(evt);
            this.dataLap = this.LaporanM.semuaLaporan();
            
            TampilModel LaporanNew = this.dataLap[this.dataLap.length - 1];
            DefaultTableModel tableModel = (DefaultTableModel) btn_submit.getModel();
            tableModel.addRow(new String[] {
                LaporanNew.ColumnD("id_pengaduan"),
                LaporanNew.ColumnD("tgl_pengaduan"),
                LaporanNew.ColumnD("status"),
            });
        }
        else{
            stackFunction.tampilAlert(this, 0, "ERROR! Laporan tidak dapat terkirim");
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private void btn_DetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DetailActionPerformed
        int pilihRow = tableLaporan.getSelectedRow();
        if(pilihRow >=0) {
            TampilModel pilihLaporan = this.dataLap[pilihRow];
            setVisible(false);
            new detailLaporan(this.user, pilihLaporan).setVisible(true);
        }else{
            stackFunction.tampilAlert(this, 0, "Tidak ada data yang dipilih");
        }
    }//GEN-LAST:event_btn_DetailActionPerformed


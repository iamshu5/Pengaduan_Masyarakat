    public void showLaporan() {
        DefaultTableModel ModelTabel = (DefaultTableModel) tableLaporan.getModel();
        for(int i=0; i<this.dataLap.length; i++){
        TampilModel sLaporan = this.dataLap[i];
            ModelTabel.addRow(new String[]{
                sLaporan.ColumnD("id_pengaduan"),
                sLaporan.ColumnD("tgl_pengaduan"),
                sLaporan.ColumnD("status"),
            });
        }
    }
    

    private void btn_registrasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrasiActionPerformed
        String validate = this.validateF();
        if(validate.isEmpty()) {
            String nik = kNik.getText();
            String namaLengkap = kNama.getText();
            String username = kUsername.getText();
            String password = kPassword.getText();
            String telepon = kTelepon.getText();
            
            Masyarakat ModelMas = new Masyarakat();
            TampilModel Akun = ModelMas.Akun(username);
            
            if(Akun==null) {
                boolean checkRegis = ModelMas.registMas(new String[] {
                    nik,
                    namaLengkap,
                    username,
                    password,
                    telepon
                });
                if(checkRegis) {
                    stackFunction.tampilAlert(this, 1, "SUCCESS! Akun Anda telah terdaftar!");
                    setVisible(false);
                    new login().setVisible(true);
                }else{
                    stackFunction.tampilAlert(this, 0, "ERROR! Akun tidak dapat terdaftar!");
                }
            }else{
                stackFunction.tampilAlert(this, 0, "UPS, Username telah terdaftar");
            }
        }else{
            stackFunction.tampilAlert(this, 0, validate);
        }
    }//GEN-LAST:event_btn_registrasiActionPerformed


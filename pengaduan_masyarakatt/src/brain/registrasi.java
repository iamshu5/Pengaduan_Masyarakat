    private String validateF() {
        if(kNik.getText().isEmpty()) {
            return "Harap masukan NIK Anda!";
        }
        if(kNama.getText().isEmpty()) {
            return "Harap masukan NAMA LENGKAP Anda!";
        }
        if(kUsername.getText().isEmpty()) {
            return "Harap masukan Username Anda!";
        }
        if(kPassword.getText().isEmpty()) {
            return "Harap masukan Password Anda!";
        }
        if(kTelepon.getText().isEmpty()) {
            return "Harap masukan Telepon Anda!";
        }    
        return "";
    }
